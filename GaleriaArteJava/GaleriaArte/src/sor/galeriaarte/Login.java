    private void bLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bLoginActionPerformed
        try {                                       
            Usuario user = new Usuario();
            Auxiliar aux = new Auxiliar();
            String jsonUser = "";
            URL u = new URL(new Juddi().getServiceUrl("Usuarios"));
            Usuarios serv = new Usuarios(u);
            jsonUser = serv.getUsuariosSoap().getUser(tbMail.getText());
            Gson gson = new GsonBuilder().create();
            user = gson.fromJson(jsonUser, Usuario.class);
            try {
                if (user.Password.equals(aux.sha1(tbPass.getText()))){
                    if(user.Privilegios == 1){
                        MainWindow m = new MainWindow(user);
                        m.setVisible(true);
                        this.setVisible(false);
                        dispose();
                    }else{
                        labelInfo.setForeground(Color.red);
                        labelInfo.setText("Error en la autentificaci√≥n");
                    }
                }else{
                    labelInfo.setForeground(Color.red);
                    labelInfo.setText("Error en la autentificaci√≥n");
                }
            } catch (NoSuchAlgorithmException ex) {
                Logger.getLogger(Login.class.getName()).log(Level.SEVERE, null, ex);
            } catch (JMSException ex) {
                Logger.getLogger(Login.class.getName()).log(Level.SEVERE, null, ex);
            }
        } catch (MalformedURLException ex) {
            Logger.getLogger(Login.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_bLoginActionPerformed

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
       try {
            Usuario user = new Usuario();
            Auxiliar aux = new Auxiliar();
            String jsonUser = "";
            URL u = new URL(new Juddi().getServiceUrl("Usuarios"));
            Usuarios serv = new Usuarios(u);
            Gson gson = new GsonBuilder().create();
            
            if(Arrays.equals(tbPass2.getPassword(), tbPass3.getPassword())){
                if(!tbPass2.getPassword().equals("") && !tbPass3.getPassword().equals("")){
                    Pattern pattern = Pattern.compile("^.+@.+\\..+$");
                    Matcher matcher = pattern.matcher(tbMail2.getText());
                    if(matcher.matches()){
                        if(!"".equals(user.Nombre)){
                            user.Nombre = tbUsuario.getText();
                            user.Password = aux.sha1(tbPass2.getText());
                            user.Mail = tbMail2.getText();
                            user.Privilegios = 1;
                            jsonUser = gson.toJson(user);


                            if(!serv.getUsuariosSoap().newUser(jsonUser)){
                                lbMEssage.setForeground(Color.red);
                                lbMEssage.setText("El usuario ya existe");
                            }else{
                                lbMEssage.setForeground(Color.green);
                                lbMEssage.setText("Usuario creado con √©xito");
                            }
                        }else{
                            lbMEssage.setForeground(Color.red);
                            lbMEssage.setText("El campo Nombre no puede estar en blanco");
                        }
                    }else{
                        lbMEssage.setForeground(Color.red);
                        lbMEssage.setText("Mail incorrecto");
                    }
                }else{
                    lbMEssage.setForeground(Color.red);
                    lbMEssage.setText("Las contrase√±as no pueden estar en blanco");
                }
            }else{
                lbMEssage.setForeground(Color.red);
                lbMEssage.setText("Los passwords no coinciden");
            }
            
        } catch (MalformedURLException ex) {
            Logger.getLogger(Login.class.getName()).log(Level.SEVERE, null, ex);
        } catch (NoSuchAlgorithmException ex) {
            Logger.getLogger(Login.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }//GEN-LAST:event_jButton1ActionPerformed

